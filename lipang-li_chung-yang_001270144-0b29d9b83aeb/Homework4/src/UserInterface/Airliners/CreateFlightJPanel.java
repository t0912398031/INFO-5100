    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        FlightSchedule fs = a.getFlightSchedule();
        Flight f = fs.addFlight();
        f.setAirplaneName(txtAirplane.getText());
        f.setFlightNumber(txtFlightNumber.getText());
        f.setDeparture(txtDeparture.getText());
        f.setArrival(txtArrival.getText());
        
        JOptionPane.showMessageDialog(null, "Airliner added successfully"); 
        txtAirplane.setText("");
        txtArrival.setText("");
        txtDeparture.setText("");
        txtFlightNumber.setText("");        
    }//GEN-LAST:event_btnSaveActionPerformed

