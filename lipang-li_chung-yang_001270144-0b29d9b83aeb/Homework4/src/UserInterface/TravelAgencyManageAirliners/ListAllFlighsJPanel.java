    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblAirliners.getModel();
        dtm.setRowCount(0);

        for(Airliner a : ad.getAirlinerDirectory()){
            for(Flight f : a.getFlightSchedule().getFlightSchedule()){
            Object[] row = new Object[5];
            row[0] = a;
            row[1] = f;
            row[2] = f.getAirplaneName();
            row[3] = f.getDeparture();
            row[4] = f.getArrival();
            f.setAirlinerName(a.getAirlinerName());
 
            dtm.addRow(row);
            }
        }
    }
