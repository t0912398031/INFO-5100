    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String departure = txtFrom.getText();
        String arrival = txtTo.getText();
        TempFlightSchedule tempFlightSchedule = ad.searchAirAirliner(ad, departure, arrival);
        
        if(tempFlightSchedule.getTempFlightSchedule().isEmpty()){
            JOptionPane.showMessageDialog(null, "Flight not found!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else{        
        SearchFromToFlighsJPanel panel = new SearchFromToFlighsJPanel(CardSequenceJPanel, tempFlightSchedule, this); 
        CardSequenceJPanel.add("SearchFromToFlighsJPanel", panel);
        CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
        layout.next(CardSequenceJPanel);
        }       
    }//GEN-LAST:event_btnSearchActionPerformed


