    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblAirliners.getModel();
        dtm.setRowCount(0);

        for(Airliner a : ad.getAirlinerDirectory()){
            Object[] row = new Object[4];
            row[0] = a;
            row[1] = a.getFleet();
            row[2] = a.getFlightSchedule();
           
            dtm.addRow(row);
        }
    }
