    public void refreshTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblEmployee.getModel();
        dtm.setRowCount(0);

        for(Employee e : business.getEd().getEmployeelist()){
            Object row[] = new Object[3];
            row[0] = e;
            row[1] = e.getLastName();
            row[2] = e.getOrganization();
            dtm.addRow(row);
        }
    }
