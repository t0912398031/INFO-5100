    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblCustomers.getModel();
        dtm.setRowCount(0);
        
        for(UserAccount c : cd.getUserAccountDirectory()){
            
            Object[] row = new Object[5];
            row[0] = c;
            row[1] = c.getID();
            row[2] = c.getAge();
            row[3] = c.getLocation();
            row[4] = c.getDestination();
 
            dtm.addRow(row);
            
        }
    }
