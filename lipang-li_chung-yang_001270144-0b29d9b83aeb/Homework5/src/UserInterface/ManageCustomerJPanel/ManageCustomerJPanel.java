    private void btnViewCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewCustomerActionPerformed
        int selectedrow = tblCustomers.getSelectedRow();

        if(selectedrow >=0){
            UserAccount c = (UserAccount)tblCustomers.getValueAt(selectedrow, 0);
            
            BrowseProductJPanel panel = new BrowseProductJPanel(CardSequenceJPanel, ad, c, this); 
            CardSequenceJPanel.add("BookAllFlighsJPanel", panel);
            CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }   // TODO add your handling code here:
    }//GEN-LAST:event_btnViewCustomerActionPerformed

