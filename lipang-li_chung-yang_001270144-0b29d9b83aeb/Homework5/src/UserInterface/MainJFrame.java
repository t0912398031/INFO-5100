    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        Boolean flag = false;
        for(UserAccount ua: business.getUad().getUserAccountDirectory()){
            if(ua.getUserName().equals(txtUserName.getText().trim())&&ua.getPassword().equals(txtPassword.getText().trim())){
                userProcessContainer.removeAll();
                if(ua.getRole().equals(UserAccount.ADMIN_ROLE)){              
                    AdminWorkAreaJPanel awajp = new AdminWorkAreaJPanel(userProcessContainer, business);
                    userProcessContainer.add("AdminWorkAreaJPanel", awajp);
                    CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                    cardLayout.next(userProcessContainer);
                    flag = true;
                    break;
                }
                else if(ua.getRole().equals(UserAccount.CUSTOMER_ROLE)){              
                    CustomerWorkAreaJPanel cwajp = new CustomerWorkAreaJPanel(userProcessContainer, business);
                    userProcessContainer.add("AdminWorkAreaJPanel", cwajp);
                    CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                    cardLayout.next(userProcessContainer);
                    flag = true;
                    break;
                }
                else if(ua.getRole().equals(UserAccount.RETAILER_ROLE)){
                    RetailerWorkAreaJPanel rwajp = new RetailerWorkAreaJPanel(userProcessContainer, business);
                    userProcessContainer.add("RetailerWorkAreaJPanel", rwajp);
                    CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                    cardLayout.next(userProcessContainer);
                    flag = true;
                    break;
                }                
            }
        }   
        if(flag == false){
            JOptionPane.showMessageDialog(this, "Invalid username/password", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        txtUserName.setEnabled(false);
        txtPassword.setEnabled(false);
        btnLogIn.setEnabled(false);
        btnLogOut.setEnabled(true);
    }//GEN-LAST:event_btnLogInActionPerformed

    private void btnLogOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogOutActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to log out?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                txtUserName.setEnabled(true);
                txtPassword.setEnabled(true);
                btnLogIn.setEnabled(true);
                btnLogOut.setEnabled(false);  
                txtPassword.setText("");
                txtUserName.setText("");

                userProcessContainer.removeAll();
                JPanel blankJPanel = new JPanel();
                userProcessContainer.add("blankJPanel", blankJPanel);
                CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                cardLayout.next(userProcessContainer);
            }            
    }//GEN-LAST:event_btnLogOutActionPerformed

