    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblRetailer.getModel();
        dtm.setRowCount(0);

        for(Retailer r : business.getRd().getRetailerDirectory()){
            Object[] row = new Object[2];
            row[0] = r;
            row[1] = r.getLocation();

     
            dtm.addRow(row);
        }
    }
