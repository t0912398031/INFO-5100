    private void btnMngProductCatalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMngProductCatalogActionPerformed
        int selectedrow = tblRetailer.getSelectedRow();

        if(selectedrow >=0){
            Retailer a = (Retailer)tblRetailer.getValueAt(selectedrow, 0);
            ManageProductCatalogJPanel panel = new ManageProductCatalogJPanel(CardSequenceJPanel, a, this);
            CardSequenceJPanel.add("ManageAirlinerJPanel", panel);
            CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }   // TODO add your handling code here:
    }//GEN-LAST:event_btnMngProductCatalogActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedrow = tblRetailer.getSelectedRow();

        if(selectedrow >=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Delete selected row?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Retailer r = (Retailer)tblRetailer.getValueAt(selectedrow, 0);
                business.getRd().deleteRetailer(r);
                JOptionPane.showMessageDialog(null, "Delete successfully");  
                populateTable();
            }            
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

