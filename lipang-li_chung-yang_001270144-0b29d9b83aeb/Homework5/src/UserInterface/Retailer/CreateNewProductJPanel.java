    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        ProductCatalog pc = r.getProductCatalog();
        Product p = pc.addProduct();
        p.setProductName(txtProductName.getText());
        p.setModelNumber(Integer.parseInt(txtModelNumber.getText()));          
        p.setPrice(Double.parseDouble(txtPrice.getText()));
        p.setAvail(Integer.parseInt(txtAvail.getText()));
        
        JOptionPane.showMessageDialog(null, "Product added successfully"); 
        txtProductName.setText("");
        txtAvail.setText("");
        txtPrice.setText("");
        txtModelNumber.setText("");        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        CardSequenceJPanel.remove(this);   
        CardLayout layout = (CardLayout) CardSequenceJPanel.getLayout();
        layout.previous(CardSequenceJPanel);
        mpcjp.populateTable();
    }//GEN-LAST:event_btnBackActionPerformed


