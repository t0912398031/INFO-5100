    public void populateEnterpriseTree(){
        DefaultTreeModel dtm = (DefaultTreeModel) EcoJTree.getModel();
        dtm.setRoot(null);
        DefaultMutableTreeNode top = new DefaultMutableTreeNode(ecoSystem);
        for(Enterprise e: ecoSystem.getEnterpriseNetwork().getEnterpriseList()){
            DefaultMutableTreeNode son = new DefaultMutableTreeNode(e);
            top.add(son);
            for(Organization o: e.getOrganizationDirectory().getOrganizationList()){
                DefaultMutableTreeNode grandson = new DefaultMutableTreeNode(o);
                son.add(grandson);
            }
        }
        dtm.setRoot(top);
    }
    
    public void populateTypeCombobox(){
        typejComboBox.removeAllItems();
        for(Enterprise.Type type:Enterprise.Type.values()){
            if(!type.equals(Enterprise.Type.SystemMantenanceEnterprise)&&!type.equals(Enterprise.Type.DepositEnterprise)){
                typejComboBox.addItem(type);
            }
        }
        
        
    }


