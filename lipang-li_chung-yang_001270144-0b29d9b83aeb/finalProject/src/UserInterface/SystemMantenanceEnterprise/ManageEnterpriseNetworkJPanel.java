    private void createEnterpriseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpriseBtnActionPerformed
        
        String eName = txtEnterpriseName.getText();
        Type type = (Type) typejComboBox.getSelectedItem();
        if(eName.isEmpty()||type == null) {
            JOptionPane.showMessageDialog(null, "Please enter the reqiured info","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        Enterprise enterprise = ecoSystem.getEnterpriseNetwork().addEnterprise(type, eName);
        // When create a new Enterprise, a admin org and financialAccount will be created as well
        DepositEnterprise.getInstance().getFinancialAccountDirectory().addFinancialAccount(eName);
        Organization AdminOrg = enterprise.getOrganizationDirectory().addOrganization(Organization.Type.Admin);
        JOptionPane.showMessageDialog(null, "New EnterPrise Created");
        populateEnterpriseTree();
        
    }//GEN-LAST:event_createEnterpriseBtnActionPerformed

