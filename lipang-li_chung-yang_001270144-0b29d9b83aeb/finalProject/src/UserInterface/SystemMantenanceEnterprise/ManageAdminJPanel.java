    private void populateTable(Enterprise enterprise){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        if(enterprise.getOrganizationDirectory().searchOrgByType(Organization.Type.Admin) == null) return;
        for (Employee employee : enterprise.getOrganizationDirectory().searchOrgByType(Organization.Type.Admin).getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[2];
            row[0] = employee.getId();
            row[1] = employee.getName();
            model.addRow(row);
        }
    }
