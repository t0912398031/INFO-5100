    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed
        
        Enterprise enterprise = (Enterprise) EnterpriseJComboBox.getSelectedItem();
        String name = nameJTextField.getText();
        String userName = uNameJTextField.getText();
        String pwd = pwdJTextField2.getText();
        if(name.isEmpty()||userName.isEmpty()||pwd.isEmpty()){
            JOptionPane.showMessageDialog(null, "please fill all the info","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(ecoSystem.checkDuplicateUserName(userName)){
            JOptionPane.showMessageDialog(null, "Username has been used","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        Employee employee = enterprise.getOrganizationDirectory().searchOrgByType(Organization.Type.Admin).getEmployeeDirectory().addEmployee(name);
        enterprise.getOrganizationDirectory().searchOrgByType(Organization.Type.Admin).getUserAccountDirectory().addUserAccount(userName, pwd, employee, new EnterpriseAdmin());
        JOptionPane.showMessageDialog(null, "Enterprise Admin created successfully");
        populateTable(enterprise);
    }//GEN-LAST:event_createJButtonActionPerformed

