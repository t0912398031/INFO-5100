    public void populateEnterpriseComboBox() {
        EntComboBox.removeAllItems();
        for (Enterprise e : ecoSystem.getEnterpriseNetwork().getEnterpriseList()) {
            EntComboBox.addItem(e);
            populateOrgComboBox(e);
        }

    }

    public void populateOrgComboBox(Enterprise enterprise) {
        orgComboBox.removeAllItems();
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            orgComboBox.addItem(organization);
            populateRoleComboBox(organization);
        }
    }

    public void populateRoleComboBox(Organization organization) {
        roleComboBox.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            roleComboBox.addItem(role);
            popContactList(role, organization);
        }
    }

    public void popContactList(Role role, Organization org) {
        
        DefaultListModel dlm = new DefaultListModel();
        dlm.removeAllElements();
        for (UserAccount u : org.getUserAccountDirectory().getUserAccountList()) {
//            if (u.getRole().equals(role) && !u.equals(userAccount)) {
            if (u.getRole().toString().equals(role.toString()) && !u.equals(userAccount)) {
                dlm.addElement(u);
            }
        }
        jListContact.setModel(dlm);

    }
    
    public void popUnreadcontact(UserAccount unreadAccount){
        
        DefaultListModel dlm = new DefaultListModel();
        dlm.removeAllElements();
        dlm.addElement(unreadAccount);
        jListContact.setModel(dlm);
        
    }

    public void popChatArea() {
        UserAccount revAccount = (UserAccount) jListContact.getSelectedValue();
        chatTxtArea.setText("");
        for (ShortMessage s : userAccount.getMessageQueue().getMessageList()) {
            if (s.getReceiver().equals(revAccount) || s.getSender().equals(revAccount)) {
                if(s.getSender().equals(revAccount))s.setStatus("Read");
                chatTxtArea.setText(chatTxtArea.getText().trim() + "\n" +s.getSender().toString() +": "+s.getMessage() + " [" + s.getRequestDate()+"] ");
            }
        }
        popUnreadMsgTab();

    }
    
    public void popUnreadMsgTab(){
        
        DefaultTableModel dtm = (DefaultTableModel) msgTab.getModel();
        dtm.setRowCount(0);
        List<ShortMessage> res = userAccount.getMessageQueue().searchUnreadMessage(userAccount);
        for(ShortMessage s:res){
            Object[] row = new Object[2];
            row[0] = s.getSender();
            row[1] = s;
            dtm.addRow(row);
        }
        
    }

