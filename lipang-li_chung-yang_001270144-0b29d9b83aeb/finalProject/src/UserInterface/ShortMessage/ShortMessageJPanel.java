    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        UserAccount revAccount = (UserAccount) jListContact.getSelectedValue();
        String mes = mesJTextField.getText();
        if (mes.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty message is not allowed", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ShortMessage sm = revAccount.getMessageQueue().addSendMessage(userAccount, revAccount, mes);
        userAccount.getMessageQueue().addMessage(sm);
        popChatArea();
        mesJTextField.setText("");
        
    }//GEN-LAST:event_sendBtnActionPerformed

    private void BCTBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCTBtnActionPerformed
        String bcm = mesJTextField.getText();
        StringBuilder sb = new StringBuilder(bcm);
        sb.append(" (Broadcasting)");
        String mes = sb.toString();
        if (mes.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty message is not allowed", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
//        List<UserAccount> revAccounts = new ArrayList<>();
        for(Enterprise e:ecoSystem.getEnterpriseNetwork().getEnterpriseList()){
            for(Organization o: e.getOrganizationDirectory().getOrganizationList()){
                for(UserAccount u:o.getUserAccountDirectory().getUserAccountList()){
                    if(!u.equals(userAccount)){
                        ShortMessage sm = u.getMessageQueue().addSendMessage(userAccount, u, mes);
                        userAccount.getMessageQueue().addMessage(sm);
//                        revAccounts.add(u);
                    }
                }
            }
        }
//        userAccount.getMessageQueue().addBroadcastMessage(userAccount, revAccounts, mes);
        popChatArea();
        mesJTextField.setText("");
    }//GEN-LAST:event_BCTBtnActionPerformed

