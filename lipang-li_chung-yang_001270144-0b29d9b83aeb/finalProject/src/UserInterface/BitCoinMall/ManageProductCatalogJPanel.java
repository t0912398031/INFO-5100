    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        
        
        int selectedrow = tblProductCatalog.getSelectedRow();

        if(selectedrow >=0){
            Product p = (Product)tblProductCatalog.getValueAt(selectedrow, 0);
            UpdateProductJPanel panel = new UpdateProductJPanel(upc, p, this); 
            upc.add("UpdateProductJPanel", panel);
            CardLayout layout = (CardLayout) upc.getLayout();
            layout.next(upc); 
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblProductCatalog.getSelectedRow();

        if(selectedrow >=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Delete selected row?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Product p = (Product)tblProductCatalog.getValueAt(selectedrow, 0);
                mallenterprise.getProductCatalog().deleteProduct(p);
                JOptionPane.showMessageDialog(null, "Delete successfully");
                populateTable();
            }            
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteActionPerformed


