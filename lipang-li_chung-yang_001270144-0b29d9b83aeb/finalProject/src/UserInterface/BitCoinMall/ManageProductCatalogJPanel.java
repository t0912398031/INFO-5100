     public ImageIcon ResizeIamge(String imagepath,byte[] pic)
    {
        ImageIcon myImage = null;
        
        if(imagepath != null)
        {
            myImage = new  ImageIcon(imagepath);
        }
            else{
                    myImage = new ImageIcon(pic);
                    }
        
        Image img = myImage.getImage();
        Image img2 = img.getScaledInstance(lbl_showimage.getWidth(), lbl_showimage.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        return image;    
    }
     

      private void displayImage(Product product)
    {
        if(product==null) return;
        if(product.getPicture()==null) return;
        if(product.getPicture().trim().isEmpty()) return;
        String path = product.getPicture();  
        lbl_showimage.setIcon(ResizeIamge(path, null));
    }

    
public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblProductCatalog.getModel();
        dtm.setRowCount(0);

        for(Product p : mallenterprise.getProductCatalog().getProductCatalog()){
            Object[] row = new Object[4];
            row[0] =p;
            row[1] =p.getId();
            row[2] =p.getPrise();
            row[3] =p.getAvail();

            dtm.addRow(row);
        
        //lbl_showimage.setIcon(ResizeIamge(path, null));
        }
    }
