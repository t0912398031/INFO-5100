    private void OK_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OK_saveActionPerformed
        // TODO add your handling code here:
        if(!txtModelNumber.getText().matches("[0-9]+")){
        JOptionPane.showMessageDialog(null, "ID is not valid","Warning",JOptionPane.WARNING_MESSAGE);
        txtProductName.setText("");
        txtAvail.setText("");
        txtPrice.setText("");
        txtModelNumber.setText("");
        return;
        }
         
         
        if(!txtPrice.getText().matches("[0-9]+(\\.){0,1}[0-9]*")){
        JOptionPane.showMessageDialog(null, "Price not valid","Warning",JOptionPane.WARNING_MESSAGE);
        txtProductName.setText("");
        txtAvail.setText("");
        txtPrice.setText("");
        txtModelNumber.setText("");
        return;
        }
          
        if(!txtAvail.getText().matches("[0-9]+")){
        JOptionPane.showMessageDialog(null, "Avaibility not valid","Warning",JOptionPane.WARNING_MESSAGE);
        txtProductName.setText("");
        txtAvail.setText("");
        txtPrice.setText("");
        txtModelNumber.setText("");
        return;
        }
        
        if(imagepath==null){
        JOptionPane.showMessageDialog(null, "Please choose a picture","Warning",JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        
      
        
        else{
        ProductCatalog pc = mallenterprise.getProductCatalog();
        Product p = pc.addProduct();
        p.setName(txtProductName.getText());
        p.setId(Integer.parseInt(txtModelNumber.getText()));          
        p.setPrise(Double.parseDouble(txtPrice.getText()));
        p.setAvail(Integer.parseInt(txtAvail.getText()));
        p.setPicture(imagepath);
        System.out.println(imagepath);
        
        JOptionPane.showMessageDialog(null, "Product added successfully");  
        txtProductName.setText("");
        txtModelNumber.setText("");
        txtPrice.setText("");
        txtAvail.setText("");
        }
    }//GEN-LAST:event_OK_saveActionPerformed


