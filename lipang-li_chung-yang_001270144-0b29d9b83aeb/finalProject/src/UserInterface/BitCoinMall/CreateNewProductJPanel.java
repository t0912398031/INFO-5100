        public ImageIcon ResizeIamge(String imagepath,byte[] pic)
    {
        ImageIcon myImage = null;
        
        if(imagepath != null)
        {
            myImage = new  ImageIcon(imagepath);
        }
            else{
                    myImage = new ImageIcon(pic);
                    }
        
        Image img = myImage.getImage();
        Image img2 = img.getScaledInstance(lbl_image.getWidth(), lbl_image.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        return image;    
    }
        
    private void but_chooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_chooserActionPerformed
        // TODO add your handling code here:
        JFileChooser file= new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.image","jpg","png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION)
        {
             File  selectedFile = file.getSelectedFile();
             String path =  selectedFile.getAbsolutePath();
             lbl_image.setIcon(ResizeIamge(path, null));
             imagepath=path;
        }
        else {
        System.out.println("No File selected");
        }
        
    }//GEN-LAST:event_but_chooserActionPerformed

