    private void PRBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PRBtn1ActionPerformed
        InvestmentEnterprise ie = (InvestmentEnterprise)enterprise;
        if(!(ie.getEarnedRecords().getRevenueList().size()>0)){
            JOptionPane.showMessageDialog(upc, "Record not found!");
            return;
        }
        else{
            AnalysisJPanel mojp = new AnalysisJPanel(upc, enterprise, ecoSystem);
            upc.add("AnalysisJPanel", mojp);
            CardLayout cardLayout = (CardLayout) upc.getLayout();
            cardLayout.next(upc);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_PRBtn1ActionPerformed


